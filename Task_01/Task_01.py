#  ***common manual testing techniques***
#
# Black_box testing:
# Testing without knowing the  internal code or logic ,just input and expected output of the functionality,
# best for validating requirements from a user’s point of view.
#
# White box testing:
# Testing with knowledge of logic and internal code,flows and conditions,Focus on the code paths, branches, loops,
# best for logic and heavy features.
#
# Gray-box testing:
# A mix of both black box and white box,best for integration testing, security checks, complex data flows.
#
# Regressive testing:
# Re-testing the existing features after changes,ensure the new code does not affect the old functionality,
# best for release validation.
#
# Unit testing:
# Technique used for testing a block of code with different paramater.
# Arrange -> set up data set.
# Act -> Execute peace of code with the prepared data set.
# Assert -> comparing expected and actual result.
#
# Smoke Testing:
# A quick,shallow test of critical functionality,best for deciding whether a build is test-worthy.
#
# Sanity Testing:
# Main focused testing on specfic fixes or changes,Confirms a bug fix or small update works,
# best for verifying hotfixes.
#
#
# ***Boundary value analysis***
# Is a black box manual testing technique that focuses on testing the edges of the input ranges.
# Instead of testing every possible value, you test values at and around the boundaries.
# 1.Minimum.
# 2.Just above minimum.
# 3.Maximum.
# 4.Just below maximum.
# 5.Sometimes just outside the valid range.
#
#
# ***Decision table testing***
# Is a black-box test design technique used when different combinations of conditions lead to different actions.
# It helps make sure no logical combination is missed.
#
# When a features’s behavior depends on multiple rules or conditions, decision tables lay everything out clearly.
# 1.Conditions → inputs or rules (Yes/No, True/False).
# 2.Actions → system behavior or output.
# 3.Each column = one test case.
#
